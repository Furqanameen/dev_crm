class ContactPolicy < ApplicationPolicy
  def index?
    admin?
  end

  def show?
    admin?
  end

  def create?
    admin?
  end

  def update?
    admin?
  end

  def destroy?
    admin?
  end

  def export?
    admin?
  end

  class Scope < Scope
    def resolve
      return scope.none unless user&.admin?
      
      scope.all
    end
  end
end
